tmp = get(hph,'position');
oph = uipanel('parent',fh,...
  'units','pixel',...
  'position',[tmp(1) tmp(2)-other_uipanel_height-5 ...
  fig_w-10 other_uipanel_height],...
  'Title','Other options',...
  'backgroundcolor',GD.col.frame);

tmp=get(oph,'position');
