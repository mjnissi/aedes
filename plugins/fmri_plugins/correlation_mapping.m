fig_w = 300;
scrsz = get(0,'screensize');
fh = figure('position',[(scrsz(3)-fig_w)/2 ...
  (scrsz(4)-fig_h)/2 fig_w fig_h],...
  'menubar','none',...
  'Name','Correlation mapping options',...
  'Numbertitle','off',...
  'closereq','uiresume(gcbf)',...
  'Color',GD.col.mainfig);

% Low pass filter options
