function [cancelled,lowpass,hipass,detrending,reserve_mean,smoothing,global_normalization,TR]=l_FilterPrompt()

GD=aedes_gui_defaults;

% Defaults
if ~ispref('Aedes','CorrelationMapping')
  hipass = [];
  lowpass = 0.08;
  detrending = true;
  smoothpr = false;
  reserve_mean = true;
  global_normalization = true;
  smoothing = [2 2 1];
  TR = 2;
