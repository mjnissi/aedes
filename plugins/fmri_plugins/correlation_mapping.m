  tmp=getpref('Aedes','CorrelationMapping');
  hipass=tmp.hipass;
  lowpass=tmp.lowpass;
  detrending=tmp.detrending;
  reserve_mean=tmp.reserve_mean;
  global_normalization=tmp.global_normalization;
  smoothing=tmp.smoothing;
  TR=tmp.TR;
end
if isempty(smoothing)
  do_smoothing = false;
else
  do_smoothing = true;
end
cancelled = false;

